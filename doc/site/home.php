<p>
DBToaster is a tool for monitoring complex queries over rapidly changing, long-lived data.  Using a variety of techniques, DBToaster transforms queries (written in SQL) into highly efficient code for monitoring the output of those queries as the underlying data changes.  
</p><p>
DBToaster generates monitoring code in several popular languages that can be easily linked directly into existing applications, avoiding the overheads of communicating with an external database engine.  DBToaster uses the native collection types for each language, making integration with existing codebases quick and easy.  
</p>

